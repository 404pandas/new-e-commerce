// sequelize import
const { Model, DataTypes } = require("sequelize");

// sequelize ORM
const sequelize = require("../config/connection");

// create categories
class Category extends Model {}

// fields + rules
Category.init(
  {
    id: {
      type: DataTypes.INTEGER,
      allowNull: false,
      primaryKey: true,
      autoIncrement: true,
    },
    category_name: {
      type: DataTypes.STRING,
      allowNull: false,
    },
  },
  {
    sequelize,
    timestamps: false,
    freezeTableName: true,
    underscored: true,
    modelName: "category",
  }
);

module.exports = Category;
